document.addEventListener('DOMContentLoaded', () => {
  // Configurar Barra Lateral
  const btnAlternar = document.getElementById('btn-alternar-barra');
  const barraLateral = document.querySelector('.dataguard-barra-lateral');
  const conteudoPrincipal = document.querySelector('.painel-conteudo-principal');

  if (btnAlternar && barraLateral && conteudoPrincipal) {
    btnAlternar.addEventListener('click', () => {
      barraLateral.classList.toggle('colapsada');
      conteudoPrincipal.classList.toggle('expandido');
    });
  }

  // Ativar Bootstrap Dropdowns para os menus de 3 pontos
  const dropdownElementList = document.querySelectorAll('.dropdown-toggle')
  const dropdownList = [...dropdownElementList].map(dropdownToggleEl => new bootstrap.Dropdown(dropdownToggleEl));
});

// Utilitário para Skeleton Loading
export const carregarDados = (elementoAlvo, htmlConteudo, delay = 1500) => {
  return new Promise((resolve) => {
    setTimeout(() => {
      if (elementoAlvo) {
        elementoAlvo.innerHTML = htmlConteudo;
      }
      resolve();
    }, delay);
  });
};

